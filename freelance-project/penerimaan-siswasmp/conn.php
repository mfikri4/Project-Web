function binary_search($data,$keyword)
{
    //mengecek apakah data array kosong?
    if(count($data) === 0) return false;
    $low = 0;
    $high = count($data) -  1;
    
    while ($low <= $high) {
        
                //calculate mid 
        $mid = floor(($low + $high) / 2);
        
                /*
                    If value we are searching found at mid position
                    then return it's index
                */
                
        if ($data[$mid] == $keyword) {

                return $mid;

        } 
        else if ($keyword < $data[$mid]) {
                
                $high = $mid - 1;
        } else if ($keyword > $data[$mid]) {
                    
                $low = $mid + 1;
        }
    }
	
    return false;

}


